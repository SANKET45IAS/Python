name="sAnket nayak"
print("1.",len(name))#Count to number of charecter present in the string(Also count the space)
print("2.",name.startswith("sAn")) #return boolean Value
print("3.",name.endswith("k")) #return boolean Value
print("4.",name.capitalize()) #return the string with capital value of 0 index
print("7.",name.title()) #returning value by Capitalize the first charecter of each word of the string
print("5.",name.lower()) #return the String with Lower Case
print("6.",name.upper()) #return the String with Upper Case
print("8.",name.count("a")) #return int value by counting a specificed charecter in a string(It is Case Sensitive)
print("9.",name.find("nayak")) #return index value of the first Occurance of that word in the string
print("9.2.",name.find("n")) #another example for clarity
print("10.",name.replace("nayak","Sidhivinayak")) #return index value of the first Occurance of that word in the string
print("11.",name.casefold()) #Converts the string to lower case


